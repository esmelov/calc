using System;

namespace Calc.Core
{
    public class Calculate
    {
        public int Addition(int a, int b)
            => a + b;

        public int Substraction(int a, int b)
            => a - b;
        
        public int Divide(int a, int b)
            => a / b;

        public int Multiply(int a, int b)
            => a * b;
    }
}
